#--------Basic plotting, overlays the actual shape with the edges--------------
def graph(input, title, overlay = None):
    
    plt.figure(figsize=(8, 6))
    plt.imshow(input, cmap="gray", origin="lower", vmin=0)

    if overlay is not None:
        plt.imshow(overlay, cmap="Reds", alpha=0.6, origin="lower")

    plt.title(title)
    plt.xlabel("X index")
    plt.ylabel("Y index")
    plt.tight_layout()
    plt.show()
